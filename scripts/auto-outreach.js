async function send(target) {
  const data = JSON.stringify({
    from: "Alex Chalunkal <alex@calcleap.com>",
    to: [target.email],
    subject: target.type === 'agency-lead' ? `AI automation for ${target.site}` : `Free Calculator Platform for ${target.site} — 2,900+ Tools`,
    html: getHtml(target)
  });
  const options = {
    hostname: 'api.resend.com', path: '/emails', method: 'POST',
    headers: { 'Content-Type': 'application/json', 'Authorization': `Bearer ${RESEND_API_KEY}` }
  };
  return new Promise((resolve, reject) => {
    const req = https.request(options, res => {
      let body = '';
      res.on('data', c => body += c);
      res.on('end', () => {
        if (res.statusCode === 200) { console.log(`✓ ${target.site} → ${target.email}`); resolve(true); }
        else if (res.statusCode === 429) { console.error(`✗ ${target.site}: 429 RATE LIMITED`); resolve('rate_limited'); }
        else { console.error(`✗ ${target.site}: ${res.statusCode}`); resolve(false); }
      });
    });
    req.on('error', () => resolve(false));
    req.write(data);
    req.end();
  });
}

(async () => {
  // Daily cap: Resend free plan allows ~100 emails/day. Hard cap at 80 to leave headroom for manual sends.
  const DAILY_CAP = 80;
  const todayStr = new Date().toISOString().slice(0, 10);
  const sentToday = Object.values(sent).filter(v => typeof v === 'string' && v.startsWith(todayStr) && !v.startsWith('failed:')).length;
  if (sentToday >= DAILY_CAP) {
    console.log(`⚠ Daily cap reached (${sentToday}/${DAILY_CAP} sent today). Skipping this cycle.`);
    process.exit(0);
  }
  const remainingBudget = DAILY_CAP - sentToday;
  const effectiveBatch = batch.slice(0, remainingBudget);
  console.log(`Auto-outreach: sending ${effectiveBatch.length} emails (${sentToday}/${DAILY_CAP} sent today)...\n`);
  let rateLimited = false;
  for (const t of effectiveBatch) {
    if (rateLimited) {
      console.log(`⏸ Skipping ${t.site} — rate limited, will retry next cycle`);
      continue;
    }
    const ok = await send(t);
    if (ok === 'rate_limited') {
      rateLimited = true;
      console.log(`⚠ Rate limited by Resend. Stopping batch. Remaining targets will retry next cycle.`);
      continue;
    }
    if (ok === true) {
      sent[t.email] = new Date().toISOString();
    } else {
      sent[t.email] = `failed:${new Date().toISOString()}`;
    }
    await new Promise(r => setTimeout(r, 5000)); // 5s between emails to avoid rate limits
  }
  fs.writeFileSync(sentFile, JSON.stringify(sent, null, 2));
  const successCount = Object.values(sent).filter(v => !String(v).startsWith('failed:')).length;
  console.log(`\nTotal sent all-time: ${successCount} (${Object.keys(sent).length} total attempts)`);
})();
